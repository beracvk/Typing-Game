package TypingGame;

public class Word {
	String text; // The text of the word
    int x, y; // The x and y coordinates of the word

    // Constructor to initialize a word with text and position
    Word(String text, int x, int y) {
        this.text = text; // Set the word text
        this.x = x; // Set the x-coordinate
        this.y = y; // Set the y-coordinate
    }

}
